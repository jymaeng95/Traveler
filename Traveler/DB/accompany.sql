CREATE TABLE GROUPACC (
    CONTENTID NUMBER,
    LEADER VARCHAR2(50),
    USERID VARCHAR2(50),
    PRIMARY KEY(CONTENTID,USERID),
    CONSTRAINT GROUP_FK FOREIGN KEY(LEADER, CONTENTID)
    REFERENCES ACCOMPANY(USERID, CONTENTID) ON DELETE CASCADE
);

